function showViz2() {
	var x = document.getElementById("v2");
	if (x.style.display === "none") {
		x.style.display = "block";
	} else {
		x.style.display = "none";
	}

	x.scrollIntoView({behavior: 'smooth', transition: '0.8s'});
}

